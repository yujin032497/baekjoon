# 학점계산

# 첫 번째 방법(학점규칙 활용)
s=['D','C','B','A']
t=input()
a=0
for i in range(len(s)):
  if s[i]==t[0]:
    if t[1]=='+':
      a=i+1+0.3
    elif t[1]=='0':
      a=i+1
    elif t[1]=='-':
      a=i+0.7
print("%.1f" % a)

